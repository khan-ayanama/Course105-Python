# Numpy Two Dimensional Array using zeros Function in Python 
import numpy as np

# Specify the shape of the 2-D array (rows, columns)
rows = 3
cols = 4

# Create a 2-D array filled with zeros
zero_array = np.zeros((rows, cols))

# Print the resulting array
print("2-D Array with Zeros:")
print(zero_array)

# Numpy Two Dimensional Array using ones Function in Python 

# Specify the shape of the 2-D array (rows, columns)
rows = 3
cols = 4

# Create a 2-D array filled with ones
ones_array = np.ones((rows, cols))

# Print the resulting array
print("2-D Array with Ones:")
print(ones_array)
