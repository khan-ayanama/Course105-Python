# zeros()
# you can create a one-dimensional NumPy array filled with zeros using the zeros function. Here's an example:

import numpy as np

# Creating a one-dimensional array filled with zeros
arr = np.zeros(5)

print("Generated Array:")
print(arr)
# output: [0. 0. 0. 0. 0.]

# In this example, np.zeros(5) creates a one-dimensional array with 5 elements, and all elements are initialized to zero.

