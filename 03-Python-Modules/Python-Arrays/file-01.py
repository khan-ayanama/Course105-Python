# Arrays
# Array is an object that provide a mechanism for storing several data items with only one identfier, it is of same data type.
# In Python Arrays can increase and decrease their sizes dynamically.
# Array can store only one type of data.
# Array and List are not same.
# Array use less memory than List.

# Example:
student_roll = ('i',[101,102,103,104])
for roll in student_roll:
    print(roll)

