# OOP (Object oriented Programming)
# It is a programming language model organized around objects rather than "action" and data rather than logic.

# Four pillars of OOP

# 1. Encapsulation
# Encapsulation is the concept of bundling the data (attributes) and the methods (functions) that operate on the data into a single unit called a class.

# 2. Abstraction
# Abstraction allows you to focus on what an object does rather than how it achieves its functionality.

# 3. Inheritance
# When new class (subclass or derived class) to inherit attributes and methods from an existing class (superclass or base class). 

# 4. Polymorphism
# It allows a new class (subclass or derived class) to inherit attributes and methods from an existing class (superclass or base class).