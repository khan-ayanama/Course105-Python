# SELF Variable

# self is a default variable that contains the memory of address of the current object.
# This varaible is used to refer all the instance varaible and method.
# The memory location is internally passed to self, as a self knows the memory address of the object so we can access variable and method of object.
# self is the first argument to any object method because the first argument is always the object reference. This is automatic, whether you call it self or not.

# Each time you create an object of a class a copy of each varaibles defined in the class is created.