# arange()
# The arange function generates evenly spaced values within a given range. Here's an example:

import numpy as np

# Creating a one-dimensional array using arange
arr = np.arange(start=1, stop=10, step=2)

print(arr)
# ouput: [1 3 5 7 9]

# start is the starting value of the sequence, stop is the end value (exclusive), and step is the step size between values.

