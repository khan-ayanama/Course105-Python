# Escape Sequence

# Escape sequences in Python

# Newline
print("Hello,\nWorld!")
# Output:
# Hello,
# World!

# Tab
print("Hello,\tPython!")
# Output:
# Hello,    Python!

# Backslash
print("This is a double backslash: \\")
# Output:
# This is a double backslash: \

# Double and Single Quote
print("She said, \"Hello!\"")
print('He\'s a programmer.')
# Output:
# She said, "Hello!"
# He's a programmer.

# Backspace
print("Hello\bWorld!")
# Output:
# HellWorld!

# Carriage Return
print("Hello\rPython!")
# Output:
# Python!lo
