# Membership operators
# THe membership operators are useful to test for membership in a sequence such as string, lists, tuples and dictionaries

# in Operator
my_list = [1, 2, 3, 4, 5]
result_in = 3 in my_list
print("in Operator:", result_in)  # Output: True

# not in Operator
my_string = "Hello, World!"
result_not_in = 'x' not in my_string
print("not in Operator:", result_not_in)  # Output: True
