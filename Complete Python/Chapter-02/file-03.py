# Bitwise Operator
# Bitwise operators are used to perform opeartions at binary digit level. These operators are not commonly used and are used in special applications whre optimized use of storage is required.

# Bitwise AND (&)
result_and = 5 & 3
print("Bitwise AND:", result_and)

# Bitwise OR (|)
result_or = 5 | 3
print("Bitwise OR:", result_or)

# Bitwise XOR (^)
result_xor = 5 ^ 3
print("Bitwise XOR:", result_xor)

# Bitwise NOT (~)
result_not = ~5
print("Bitwise NOT:", result_not)

# Left Shift (<<)
result_left_shift = 5 << 1
print("Left Shift:", result_left_shift)

# Right Shift (>>)
result_right_shift = 5 >> 1
print("Right Shift:", result_right_shift)
