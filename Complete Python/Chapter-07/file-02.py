# Escape Sequence
# Escape sequences start with a backslash \. Here are some common escape sequences used in Python strings:

# \n - Newline:
print("Hello\nWorld")

# \t - Tab:
print("This is\tTabbed")

# \ - Backslash:
print("This is a backslash: \\")

# ' - Single Quote:
print('He said, \'Hello!\'')

# " - Double Quote:
print("She said, \"Hi!\"")

# \b - Backspace:
print("Back\bSpace")

# \r - Carriage Return:


print("Carriage\rReturn")

# \x - Hexadecimal Escape:
print("\x48\x65\x6C\x6C\x6F")  # Hex values for 'Hello'
