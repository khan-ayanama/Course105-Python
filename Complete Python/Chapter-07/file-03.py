# String Methods and Operations

# LENGTH OF STRING
my_string = "Hello, World!"

# Find the length of the string
length = len(my_string)


# LOOP WITH STRING
my_string = "Hello, World!"

# Using a for loop to access each character
for char in my_string:
    print(char)

for i in range(len(my_string)):
    print(my_string[i])

