# Data Types
# 1. Built-in
# 2. User Defined

# BUILT_IN_DATATYPE
# These data types are provided by Python Language

# Numeric Types:
# int: Integer type represents whole numbers
x = 5

# float: Floating-point type represents numbers with decimal points.
y = 3.14

# complex: Complex numbers with a real and imaginary part.
z = 2 + 3j

# Sequence Types:
# str: String type represents sequences of characters.
my_string = "Hello, Python!"

# list: List type represents ordered, mutable sequences.
my_list = [1, 2, 3, 4]

# tuple: Tuple type represents ordered, immutable sequences.
my_tuple = (1, 2, 3, 4)

# Set Types:
# set: Set type represents an unordered collection of unique elements.
my_set = {1, 2, 3, 4}

# frozenset: Frozenset type represents an immutable set.
my_frozenset = frozenset([1, 2, 3, 4])

# Mapping Type:
# dict: Dictionary type represents a collection of key-value pairs.
my_dict = {'key1': 'value1', 'key2': 'value2'}

# Boolean Type:
# bool: Boolean type represents either True or False.
is_true = True
is_false = False

# None Type:
# NoneType: Represents the absence of a value or a null value.
my_variable = None


